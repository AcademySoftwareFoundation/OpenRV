        inline int betterMod(int a, int by)
        {
            int rem = a % by;
            return rem < 0 ? rem + by : rem;
        }

        //******************************************************************************
        inline int getIndex(int x) { return (int)perm[betterMod(x, 256)]; }

        //******************************************************************************
        inline int getIndex(int x, int y) { return getIndex(getIndex(x) + y); }

        //******************************************************************************
        inline int getIndex(int x, int y, int z)
        {
            return getIndex(getIndex(getIndex(x) + y) + z);
        }

        //******************************************************************************
        inline int getIndex(int x, int y, int z, int w)
        {
            return getIndex(getIndex(getIndex(getIndex(x) + y) + z) + w);
        }

        //******************************************************************************
        inline float cubic(float t) { return (3.0f - 2.0f * t) * t * t; }

        //******************************************************************************
        inline float cubicGrad(float t) { return (6.0f - 6.0f * t) * t; }

