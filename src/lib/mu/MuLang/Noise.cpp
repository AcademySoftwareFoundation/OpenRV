    //******************************************************************************
    float noise1(float x)
    {
        // Get cell coordinates
        int ix = (int)floor(x);
        float fx = x - (float)ix;

        // Get corner gradients
        float* grad0 = grads[getIndex(ix)];
        float* grad1 = grads[getIndex(ix + 1)];

        // Projections, and their gradients
        float g0 = grad0[0] * fx;
        float g1 = grad1[0] * (fx - 1.0f);

        // X interpolations
        return g0 + cubic(fx) * (g1 - g0);
    }

